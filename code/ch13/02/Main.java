public class Main {
    static int addition3(int a, int b, int c) {
        return a + b + c;
    }
    public static void main(String[] args) {
        System.out.println(addition3(10, 20, 25));
    }
}

/*
บรรทัดที่ 01 : ประกาศ class ชื่อ Main
บรรทัดที่ 02 : ประกาศฟังก์ชันชื่อ addition ที่รับ parameter เป็น a, b และ c 
บรรทัดที่ 03 : return ค่าผลบวกของ a, b และ c 
บรรทัดที่ 05 : ฟังก์ชัน main โปรแกรมจะเริ่มรันฟังก์ชันนี้เป็นฟังก์ชันแรก
บรรทัดที่ 06 : print ค่าที่ได้จาก addition3(10, 20, 25)
*/