public class Main {
    static int addition(int a, int b) {
        return a + b;
    }
    public static void main(String[] args) {
        System.out.println(addition(18, 32));
    }
}

/*
บรรทัดที่ 01 : ประกาศ class ชื่อ Main
บรรทัดที่ 02 : ประกาศฟังก์ชันชื่อ addition ที่รับ parameter เป็น a และ b
บรรทัดที่ 03 : return ค่าผลบวกของ a และ b 
บรรทัดที่ 05 : ฟังก์ชัน main โปรแกรมจะเริ่มรันฟังก์ชันนี้เป็นฟังก์ชันแรก
บรรทัดที่ 06 : print ค่าที่ได้จาก addition(18, 32)
*/